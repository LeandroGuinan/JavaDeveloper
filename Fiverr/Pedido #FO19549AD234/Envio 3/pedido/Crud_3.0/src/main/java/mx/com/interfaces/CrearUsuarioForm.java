    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        UsuariosService servicio = new UsuariosService();
        if (nombreText.getText().equals("") || apellidoText.getText().equals("") || edadText.getText().equals("")
                || emailText.getText().toString().equals("") || telefono1Text.getText().toString().equals("") || telefono2Text.getText().toString().equals("")
                || departamentoText.getText().toString().equals("") || nombreUsuarioText.getText().toString().equals("") || contraText.getText().toString().equals("")) {
            JOptionPane.showMessageDialog(null, "rellena todos los campos", "Error", JOptionPane.ERROR_MESSAGE);

        } else {

            try {
                if (Integer.parseInt(edadText.getText()) > 0 && Integer.parseInt(edadText.getText()) < 100) {
                    Persona psp = new Persona(nombreText.getText(), apellidoText.getText(), Integer.parseInt(edadText.getText()));
                    AgregarMail ag = new AgregarMail();
                    if (ag.validar(emailText.getText()) == true) {
                        Empleado emp = new Empleado(emailText.getText(), telefono1Text.getText(), telefono2Text.getText(), departamentoText.getText(), psp);
                        Rol rl = new Rol();
                        if (rolBox.getSelectedItem().toString().equals("Usuario")) {
                            rl = new Rol(2);
                        } else if (rolBox.getSelectedItem().toString().equals("Administrador")) {
                            rl = new Rol(1);
                        }
                        Usuario usu = new Usuario(nombreUsuarioText.getText(), contraText.getText(), emp, rl);
                        servicio.insertarUsuario(usu);
                        JOptionPane.showMessageDialog(null, "Usuario Creado", "Info", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(null, "Ingresa un Email Valido", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Ingresa Una Edad valida", "Error", JOptionPane.ERROR_MESSAGE);
                }

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Ingresa Valores validos", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

